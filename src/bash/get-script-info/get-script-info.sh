#!/usr/bin/env bash

# -------------------------------------------------------------------------------- #
# Description                                                                      #
# -------------------------------------------------------------------------------- #
# This is a simple script to show how to get get the script base path and name.    #
# -------------------------------------------------------------------------------- #

# -------------------------------------------------------------------------------- #
# Get Script Name                                                                  #
# -------------------------------------------------------------------------------- #
# Work out the name to the script, not matter where it is invoked from.            #
# -------------------------------------------------------------------------------- #

get_script_name()
{
    local script_name

    script_name=$(basename "$0")
    echo "${script_name}"
}

# -------------------------------------------------------------------------------- #
# Get Script Path                                                                  #
# -------------------------------------------------------------------------------- #
# Work out the path to the script, not matter where it is invoked from.            #
# -------------------------------------------------------------------------------- #

get_script_path()
{
    local script_path

    script_path="$( cd -P "$(dirname "${BASH_SOURCE[0]}")" && pwd )"
    echo "${script_path}"
}

# -------------------------------------------------------------------------------- #
# Run Tests                                                                        #
# -------------------------------------------------------------------------------- #
# A VERY simple test function to ensure that it all works                          #
# -------------------------------------------------------------------------------- #

run_test()
{
    name=$(get_script_name)
    echo "Name = $name"

    path=$(get_script_path)
    echo "Path = $path"

}

# -------------------------------------------------------------------------------- #
# Main()                                                                           #
# -------------------------------------------------------------------------------- #
# This is the actual 'script' and the functions/sub routines are called in order.  #
# -------------------------------------------------------------------------------- #

run_test

# -------------------------------------------------------------------------------- #
# End of Script                                                                    #
# -------------------------------------------------------------------------------- #
# This is the end - nothing more to see here.                                      #
# -------------------------------------------------------------------------------- #

